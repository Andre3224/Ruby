#Diferente do IF e Else que verifica e executa 
#uma condição VERDADEIRA, 
#esta estrutura (Unless) executa quando a 
#condição for FALSA

x = 10

unless x > 15 # a menos que x seja maior que 15 faça isso
    puts "x é menor ou igual a 15"
else #se não for faça aquilo
    puts "x é maior que 15"
end
